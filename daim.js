var input1 = prompt("Enter number 1 ");
var input2 = prompt("Enter number 2 ");

var operator = prompt("please select a mathimtaical operator either +, -, /, *");


switch(operator){

    case "+":

        var result = parseInt(input1) + parseInt(input2);

        alert(result);
        break;

        case "-":
            var result = parseInt(input1) - parseInt(input2);

alert(result);
            break;

            case "*" :
                var result = parseInt(input1) * parseInt(input2);

alert(result);
                break;

                case "/":
                    var result = parseInt(input1) / parseInt(input2);

alert(result);

                break;

                default : alert("User Syntax Error");
}












